package com.basic;

public class OperatorsDemo {

	public static void main(String[] args) {

		int a = 10; // local variable

		System.out.println(a);

		System.out.println(++a);

		System.out.println(a++);

		System.out.println(a);

		System.out.println(--a); // 11

		System.out.println(a--); // 11

		System.out.println(a); // 10
	}

}
